    public void preencherPlaca(String letraNumPlaca) {
        switch (this.baseJFrame.getAutomacaoCTR().getQtde()) {
            case 2:
                chamaTecladoNum();
                break;
            case 3:
                chamaTecladoLetraNum();
                break;
            case 4:
                chamaTecladoNum();
                break;
            default:
                break;
        }
        jLabelPlaca.setText(this.baseJFrame.getAutomacaoCTR().preencherPlaca(letraNumPlaca));
    }

    public void apagarPlaca() {
        switch (this.baseJFrame.getAutomacaoCTR().getQtde()) {
            case 3:
                chamaTecladoLetra();
                break;
            case 4:
                chamaTecladoNum();
                break;
            case 5:
                chamaTecladoLetraNum();
                break;
            case 6:
                chamaTecladoNum();
                break;
            default:
                break;
        }
        jLabelPlaca.setText(this.baseJFrame.getAutomacaoCTR().apagarPlaca());
    }

    public void ok() {

        if (this.baseJFrame.getAutomacaoCTR().getQtde() == 7) {

            this.baseJFrame.getAutomacaoCTR().getDadosCarregBean().setPlaca1(this.baseJFrame.getAutomacaoCTR().getTextoPlaca());
            this.baseJFrame.getAutomacaoCTR().getDadosCarregBean().setPlaca2("");
            this.baseJFrame.getAutomacaoCTR().getDadosCarregBean().setPlaca3("");
            this.baseJFrame.mudarTela("TelaCameraJPanel");

        } else {

            terminarPlaca();

        }
    }

    public void terminarPlaca() {
        defaultJDialog.setTxtMsg("Por Favor, termine de digitar a placa do VeÃ­culo.");
        Timer timer = new Timer(4000, (ActionEvent e) -> {
            defaultJDialog.setVisible(false);
        });
        timer.setRepeats(false);
        timer.start();
        defaultJDialog.setVisible(true);
    }

    private void chamaTecladoLetra() {

        TecladoLetraJPanel jPanelTecladoLetra = new TecladoLetraJPanel(this);
        jPanelTecladoLetra.setSize(820, 285);
        jPanelTecladoLetra.setLocation(0, 0);

        jPanelPrincPlaca.removeAll();
        jPanelPrincPlaca.add(jPanelTecladoLetra, BorderLayout.CENTER);
        jPanelPrincPlaca.revalidate();
        jPanelPrincPlaca.repaint();

    }

